int		ft_cubes_count(char *file)
{
    int			i;
    int			n;
    i = 0;
    n = 0;
    while (file[i] != '\0')
    {
        if ((file[i] == '\n' && file[i + 1] == '\n'))
            n++;
        i++;
    }
	return (n + 1);
}
